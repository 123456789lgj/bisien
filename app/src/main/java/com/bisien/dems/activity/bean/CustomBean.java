package com.bisien.dems.activity.bean;

public class CustomBean {
    private String equipmentName;
    private String equipmentCounts;
    private String runEquipmentCounts;
    private String equipmentAlarm;
    private String category;

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getEquipmentName() {
        return equipmentName;
    }

    public void setEquipmentName(String equipmentName) {
        this.equipmentName = equipmentName;
    }

    public String getEquipmentCounts() {
        return equipmentCounts;
    }

    public void setEquipmentCounts(String equipmentCounts) {
        this.equipmentCounts = equipmentCounts;
    }

    public String getRunEquipmentCounts() {
        return runEquipmentCounts;
    }

    public void setRunEquipmentCounts(String runEquipmentCounts) {
        this.runEquipmentCounts = runEquipmentCounts;
    }

    public String getEquipmentAlarm() {
        return equipmentAlarm;
    }

    public void setEquipmentAlarm(String equipmentAlarm) {
        this.equipmentAlarm = equipmentAlarm;
    }
}
